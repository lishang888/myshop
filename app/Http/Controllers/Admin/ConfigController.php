    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
        $res = Config::destroy($id);
        
        if ($res) {
            return redirect('admin/config')->with('success','删除成功');
        }else{
            return back()->with('error','删除失败');
        }
